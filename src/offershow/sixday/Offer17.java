/**
 * 含所有字符的最短字符串
 *
 * @author 张烈文
 * @date 2021/9/23 14:43
 */
public class Offer17 {
    public String minWindow(String s, String t) {
        int n = s.length();
        int m = t.length();
        if (n < m) {
            return "";
        }

        return "";

    }
}
